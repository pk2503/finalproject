public class Account {
    String Name;
    String Password;
    String Sex;
    int ID;
    String DOB;

    public Account(String Name, String Password, int ID, String Sex, String DOB) {
        this.Name = Name;
        this.Password = Password;
        this.ID = ID;
        this.Sex = Sex;
        this.DOB = DOB;
    }
}
